package day0105;

public class JumsuTest {
	public static void main(String[] args) {
		int jumsu = 85;
		char grade;

		switch (jumsu / 10) {
		case 10:
		case 9:
			grade = 'A';
			break;
		case 8:
			grade = 'B';
			break;
		case 7:
			grade = 'C';
			break;
		case 6:
			grade = 'D';
			break;
		default:
			grade = 'F';
		}
		System.out.println(grade + "학점입니다!!");
	}

}
